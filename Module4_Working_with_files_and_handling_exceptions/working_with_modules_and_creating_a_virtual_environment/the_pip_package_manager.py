# For example, to display a list of installed packages:
# python -m pip list

# Often, the system specifies that pip is launched using Python, and you can call pip directly:
# pip list

# To install the latest version of a package, using the requests package as an example:
# pip install requests

# To install a specific version of the requests package:
# pip install requests==2.28.2

# To install a version of the requests package newer than 2.28.2:
# pip install requests>=2.28.2

# To install a version of the requests package older than 2.28.2:
# pip install requests<=2.28.2

# To remove the requests package:
# pip uninstall requests

# List of installed packages with versions:
# pip freeze